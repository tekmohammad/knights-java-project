package tek.week_2.day_1;

public class ArithmeticOperators {
	
	public static void main(String[] args) {
		
		/*
		 * Arithmetic Operators 
		 * + addition
		 * - subtraction
		 * * multiplication 
		 * / division
		 * % Remainder of Division
		 */
		int addition = 10 + 20 + 30 + 40 + 50; 
		
		double subtraction = 20.19 - 1.43;
		
		double multiplication = 30 * 2.5; 
		
		double division = 12.0 / 5.0; 
		
		int remainder = 9 % 2;
		
		System.out.println(addition);
		System.out.println(subtraction);
		System.out.println(multiplication);
		System.out.println(division);
		System.out.println(remainder);
		
		int appleQuantity = 20; 
		double applePrice = 1.23; 
		double total = appleQuantity * applePrice; 
		System.out.println(total);
	}

}
