package tek.week_3.day_2;

public class RelationalOperators {
	
	public static void main(String[] args) {
		
		// Relational Operators  
		// > < >= <= == !=
		boolean isGreaterThan = 10 > 20; 
		
		boolean isLessThan = 20 < 100; 
		
		boolean isGreaterThanEqual = 50 >= 100; 
		
		boolean isLessThanEqual = 16 <= 16; 
		
		boolean isEqual = 100 == 200; 
		
		boolean isNotEqual = 18 != 16;
	}

}
