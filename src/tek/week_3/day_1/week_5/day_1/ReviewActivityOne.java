package tek.week_5.day_1;

public class ReviewActivityOne {
	public static void main(String[] args) {
		//Write a java program to print the numbers starting from 
		// 0 to 10 using While LOOP.
		int number = 0;
		while (number <= 10) {
			System.out.println("The value of number at iteration " + number
					+ " is = " + number);
			number++;
		}
	}
}
