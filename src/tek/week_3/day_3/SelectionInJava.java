package tek.week_3.day_3;

public class SelectionInJava {
	
	//Selection In Java 
	// IF and ELSE (work with 1 or more conditions). 
	// SWITCH (Switching between cases.) 
	public static void main(String[] args) {
		double shirtPrice = 69.52; 
		
		if (shirtPrice < 50) {
			System.out.println("Pay for shirt");
		}else {
			System.out.println("Do NOT buy this shirt");
		}
		
		int score = 60; 
		
		if (score >= 50) {
			System.out.println("You passed");
		}else {
			System.out.println("You failed");
		}
		
	}

}
